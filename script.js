function updPage(elementPath, newText){
    $(elementPath).fadeOut(fadems, function() {
        $(this).css("margin-top", "0px")
        $(this).css("margin-bottom", "0px")
        $(this).html(newText).fadeIn(fadems);
    });
    $('.footer').fadeOut(fadems, function() {
        $(this).fadeIn(fadems);
    });
}